function buildDeepMemoryDisplayItems(memories = []) {
    const orderedMemories = [...memories].reverse();
    const items = [];

    for (let index = 0; index < orderedMemories.length; index++) {
        const current = orderedMemories[index];
        const currentText = String(current?.text || '').trim();
        if (!currentText) continue;

        const next = orderedMemories[index + 1];
        const nextText = String(next?.text || '').trim();
        const currentTone = String(current?.tone || '');
        const nextTone = String(next?.tone || '');
        const isDualTonePair = nextText
            && currentText === nextText
            && ((currentTone === 'positive' && nextTone === 'negative') || (currentTone === 'negative' && nextTone === 'positive'));

        if (isDualTonePair) {
            const dualItem = { text: currentText, tone: 'dual' };
            if (current?.msgIndex !== undefined) { dualItem.msgIndex = current.msgIndex; dualItem.updateIdx = current.updateIdx; }
            // Both pills in a dual-tone pair carry the SAME affinityDelta /
            // romanceDelta (they come from the same social update).  Do NOT
            // sum them — that would double the values.  Take from current.
            if (current?.affinityDelta !== undefined) dualItem.affinityDelta = current.affinityDelta;
            if (current?.romanceDelta !== undefined) dualItem.romanceDelta = current.romanceDelta;
            items.push(dualItem);
            index++;
            continue;
        }

        const singleItem = { text: currentText, tone: currentTone };
        if (current?.msgIndex !== undefined) { singleItem.msgIndex = current.msgIndex; singleItem.updateIdx = current.updateIdx; }
        if (current?.affinityDelta !== undefined) singleItem.affinityDelta = current.affinityDelta;
        if (current?.romanceDelta !== undefined) singleItem.romanceDelta = current.romanceDelta;
        items.push(singleItem);
    }

    return items;
}

function buildMemoryDeltaBadge(memory = {}) {
    const affDelta = parseInt(memory?.affinityDelta, 10);
    const romDelta = parseInt(memory?.romanceDelta, 10);
    const hasAff = Number.isFinite(affDelta) && affDelta !== 0;
    const hasRom = Number.isFinite(romDelta) && romDelta !== 0;
    if (!hasAff && !hasRom) return '';
    let html = '<span class="bb-mem-deltas">';
    if (hasAff) {
        const cls = affDelta > 0 ? 'bb-mem-delta-aff-pos' : 'bb-mem-delta-aff-neg';
        html += `<span class="${cls}">${affDelta > 0 ? '+' : ''}${affDelta}</span>`;
    }
    if (hasRom) {
        const cls = romDelta > 0 ? 'bb-mem-delta-rom-pos' : 'bb-mem-delta-rom-neg';
        html += `<span class="${cls}">${romDelta > 0 ? '+' : ''}${romDelta}</span>`;
    }
    html += '</span>';
    return html;
}

function renderDeepMemoryPill(memory = {}) {
    const text = escapeHtml(memory?.text || '');
    const tone = String(memory?.tone || '');
    if (!text) return '';
    const sourceData = memory?.msgIndex !== undefined ? `data-msg-idx="${memory.msgIndex}" data-update-idx="${memory.updateIdx}"` : '';
    const editBtn = sourceData ? '<button type="button" class="bb-memory-edit-btn" title="Редактировать"><i class="fa-solid fa-pen-to-square"></i></button>' : '';
    const deleteBtn = sourceData ? '<button type="button" class="bb-memory-delete-btn" title="Удалить"><i class="fa-solid fa-trash-can"></i></button>' : '';
    const btns = (editBtn || deleteBtn) ? `<span class="bb-memory-pill-btns">${editBtn}${deleteBtn}</span>` : '';
    const deltaBadge = buildMemoryDeltaBadge(memory);
    if (tone === 'dual') {
        return `<div class="bb-memory-pill deep dual-tone" ${sourceData} title="Противоречивое незабываемое событие"><span>${text}</span>${deltaBadge}${btns}</div>`;
    }
    return `<div class="bb-memory-pill deep ${tone}" ${sourceData}><span>${text}</span>${deltaBadge}${btns}</div>`;
}

function getCharacterInitials(charName = '') {
    const parts = String(charName || '').trim().split(/\s+/).filter(Boolean);
    if (parts.length === 0) return '?';
    return parts.slice(0, 2).map(part => part[0] || '').join('').toUpperCase();
}

function buildAvatarStyle(profile = {}) {
    const avatar = String(profile?.avatar || '').trim();
    if (!avatar) return '';

    const safeAvatar = avatar.replace(/'/g, "\\'");
    return `background-image:url('${safeAvatar}'); background-position:center; background-size:cover;`;
}

