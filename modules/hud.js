async function loadImageFromUrl(dataUrl = '') {
    return await new Promise((resolve, reject) => {
        const img = new Image();
        img.onload = () => resolve(img);
        img.onerror = () => reject(new Error('IMAGE_LOAD_ERROR'));
        img.src = String(dataUrl || '');
    });
}

async function resizeImageFileToDataUrl(file, maxSide = 2200) {
    const dataUrl = await new Promise((resolve, reject) => {
        const reader = new FileReader();
        reader.onload = () => resolve(String(reader.result || ''));
        reader.onerror = () => reject(new Error('FILE_READ_ERROR'));
        reader.readAsDataURL(file);
    });

    const image = await loadImageFromUrl(dataUrl);

    const width = image.naturalWidth || image.width || 0;
    const height = image.naturalHeight || image.height || 0;
    if (!width || !height) return dataUrl;

    const scale = Math.min(1, maxSide / Math.max(width, height));
    const targetWidth = Math.max(1, Math.round(width * scale));
    const targetHeight = Math.max(1, Math.round(height * scale));
    if (scale === 1) return dataUrl;

    const canvas = document.createElement('canvas');
    canvas.width = targetWidth;
    canvas.height = targetHeight;
    const context = canvas.getContext('2d');
    if (!context) return dataUrl;
    context.drawImage(image, 0, 0, targetWidth, targetHeight);

    try {
        return canvas.toDataURL('image/webp', 0.92);
    } catch (error) {
        void error;
        return canvas.toDataURL('image/png');
    }
}

async function createCroppedAvatarDataUrl(sourceDataUrl = '', crop = {}, options = {}) {
    if (!String(sourceDataUrl || '').trim()) return '';

    const image = await loadImageFromUrl(sourceDataUrl);
    const naturalWidth = image.naturalWidth || image.width || 0;
    const naturalHeight = image.naturalHeight || image.height || 0;
    if (!naturalWidth || !naturalHeight) return String(sourceDataUrl || '');

    const outputWidth = Math.max(180, parseInt(options.outputWidth, 10) || 360);
    const outputHeight = Math.max(220, parseInt(options.outputHeight, 10) || 440);
    const focusX = Math.max(0, Math.min(100, Number(crop?.x) || 50)) / 100;
    const focusY = Math.max(0, Math.min(100, Number(crop?.y) || 50)) / 100;
    const zoom = Math.max(100, Math.min(320, Number(crop?.zoom) || 100)) / 100;

    const baseScale = Math.max(outputWidth / naturalWidth, outputHeight / naturalHeight);
    const effectiveScale = baseScale * zoom;
    const visibleWidth = outputWidth / effectiveScale;
    const visibleHeight = outputHeight / effectiveScale;

    const centerX = naturalWidth * focusX;
    const centerY = naturalHeight * focusY;
    const maxLeft = Math.max(0, naturalWidth - visibleWidth);
    const maxTop = Math.max(0, naturalHeight - visibleHeight);
    const cropLeft = Math.min(maxLeft, Math.max(0, centerX - (visibleWidth / 2)));
    const cropTop = Math.min(maxTop, Math.max(0, centerY - (visibleHeight / 2)));
    const cropWidth = Math.max(1, Math.min(visibleWidth, naturalWidth));
    const cropHeight = Math.max(1, Math.min(visibleHeight, naturalHeight));

    const canvas = document.createElement('canvas');
    canvas.width = outputWidth;
    canvas.height = outputHeight;
    const context = canvas.getContext('2d');
    if (!context) return String(sourceDataUrl || '');

    context.imageSmoothingEnabled = true;
    context.imageSmoothingQuality = 'high';
    context.drawImage(
        image,
        cropLeft,
        cropTop,
        cropWidth,
        cropHeight,
        0,
        0,
        outputWidth,
        outputHeight,
    );

    try {
        return canvas.toDataURL('image/webp', 0.88);
    } catch (error) {
        void error;
        return canvas.toDataURL('image/jpeg', 0.9);
    }
}

function syncCharacterEditorAvatarPreview(editor) {
    const preview = editor.find('.bb-avatar-preview');
    if (preview.length === 0) return;

    const avatar = String(editor.find('.bb-edit-avatar-source').val() || editor.find('.bb-edit-avatar-data').val() || '').trim();
    const crop = {
        x: parseFloat(String(editor.find('.bb-avatar-focus-x').val() || '50')),
        y: parseFloat(String(editor.find('.bb-avatar-focus-y').val() || '50')),
        zoom: parseFloat(String(editor.find('.bb-avatar-focus-zoom').val() || '100')),
    };
    const charName = String(editor.closest('.bb-char-card').attr('data-char') || '').trim();
    const requestId = Number(editor.data('bbAvatarPreviewRequestId') || 0) + 1;

    editor.data('bbAvatarPreviewRequestId', requestId);
    preview.find('.bb-avatar-preview-initials').text(getCharacterInitials(charName));
    editor.find('.bb-btn-clear-avatar').prop('disabled', !avatar);

    if (!avatar) {
        preview.removeAttr('style');
        preview.removeClass('has-image').addClass('is-empty');
        return;
    }

    preview.removeClass('is-empty');
    createCroppedAvatarDataUrl(avatar, crop, {
        outputWidth: AVATAR_PREVIEW_WIDTH,
        outputHeight: AVATAR_PREVIEW_HEIGHT,
    }).then((previewDataUrl) => {
        if (Number(editor.data('bbAvatarPreviewRequestId') || 0) !== requestId) return;
        const previewStyle = buildFixedAvatarPreviewStyle(previewDataUrl);
        if (!previewStyle) {
            preview.removeClass('has-image').addClass('is-empty').removeAttr('style');
            return;
        }
        preview.removeClass('is-empty').addClass('has-image').attr('style', previewStyle);
    }).catch((error) => {
        if (Number(editor.data('bbAvatarPreviewRequestId') || 0) !== requestId) return;
        console.error('[BB VN] Avatar preview sync failed:', error);
        preview.removeClass('has-image').addClass('is-empty').removeAttr('style');
    });
}

function queueCharacterEditorAvatarPreview(editor, immediate = false) {
    const activeTimer = Number(editor.data('bbAvatarPreviewTimer') || 0);
    if (activeTimer) {
        window.clearTimeout(activeTimer);
        editor.removeData('bbAvatarPreviewTimer');
    }

    if (immediate) {
        syncCharacterEditorAvatarPreview(editor);
        return;
    }

    const timerId = window.setTimeout(() => {
        if (!editor.closest('body').length) return;
        editor.removeData('bbAvatarPreviewTimer');
        syncCharacterEditorAvatarPreview(editor);
    }, AVATAR_PREVIEW_DEBOUNCE_MS);

    editor.data('bbAvatarPreviewTimer', timerId);
}

/**
 * Smoothly scrolls to a target element inside the HUD after a CSS transition completes.
 * Waits for `transitionend` on the `transitionEl` (e.g. .bb-char-body or .bb-char-editor),
 * then calculates the scroll offset to center `targetSelector` inside `.bb-hud-content`.
 * Falls back to a 300ms timeout if `transitionend` never fires.
 */
function scrollAfterTransition(transitionEl, targetSelector, card) {
    if (!transitionEl) return;

    const doScroll = () => {
        // If targeting the card itself, use card[0] directly (find only searches descendants)
        let target;
        if (targetSelector === '.bb-char-card' && card) {
            target = card[0];
        } else {
            target = (card && card.find(targetSelector)[0]) || transitionEl;
        }
        const scroller = document.querySelector('.bb-hud-content.active');
        if (!target || !scroller) return;

        const targetRect = target.getBoundingClientRect();
        const scrollerRect = scroller.getBoundingClientRect();
        const offset = targetRect.top - scrollerRect.top + scroller.scrollTop
            - (scrollerRect.height / 2) + (targetRect.height / 2);
        scroller.scrollTo({ top: offset, behavior: 'smooth' });
    };

    let done = false;
    const finish = () => {
        if (done) return;
        done = true;
        transitionEl.removeEventListener('transitionend', onEnd);
        clearTimeout(fallback);
        doScroll();
    };

    const onEnd = (e) => {
        // Only react to the main animated property
        if (e.target !== transitionEl) return;
        if (e.propertyName === 'grid-template-rows' || e.propertyName === 'max-height') {
            finish();
        }
    };

    transitionEl.addEventListener('transitionend', onEnd);
    // Fallback in case transitionend doesn't fire (e.g. no transition happened)
    const fallback = setTimeout(finish, 300);
}

function setCharacterCardExpanded(card, shouldExpand, options = {}) {
    card.toggleClass('expanded', Boolean(shouldExpand));
    if (!shouldExpand) {
        card.removeClass('editor-open');
        // Reset all "show more" sections back to default collapsed state
        card.find('.bb-memory-show-more').each(function() {
            const btn = jQuery(this);
            const section = btn.closest('.bb-memory-section');
            const total = parseInt(btn.attr('data-total') || '0', 10);
            // Re-hide items beyond the limit
            const allPills = section.find('.bb-memory-pill');
            allPills.each(function(idx) {
                if (idx >= MEMORY_SHOW_LIMIT) {
                    jQuery(this).addClass('bb-memory-hidden');
                }
            });
            // Reset button state
            btn.removeClass('bb-memory-show-more-done');
            btn.attr('data-shown', String(MEMORY_SHOW_LIMIT));
            const remaining = total - MEMORY_SHOW_LIMIT;
            if (remaining > 0) {
                btn.html(`<i class="fa-solid fa-chevron-down"></i>ещё ${remaining}`);
            }
        });
        // Smooth scroll to the card itself after collapse animation completes
        scrollAfterTransition(card.find('.bb-char-body')[0], '.bb-char-card', card);
    } else {
        // Reset log-collapsed when expanding
        card.removeClass('log-collapsed');
        if (!options?.skipScroll) {
            // Smooth scroll to the body after expand animation completes
            scrollAfterTransition(card.find('.bb-char-body')[0], '.bb-char-body-inner', card);
        }
    }
}

function setCharacterEditorOpen(card, shouldOpen, options = {}) {
    void options;
    if (shouldOpen) {
        setCharacterCardExpanded(card, true, { ...options, skipScroll: true });
        card.addClass('editor-open');
        // Reset log-collapsed when editor opens
        card.removeClass('log-collapsed');
        queueCharacterEditorAvatarPreview(card.children('.bb-char-editor'), true);
        // Auto-resize description textarea
        const descInput = card.find('.bb-edit-description-input')[0];
        if (descInput) {
            const resize = () => { descInput.style.height = 'auto'; descInput.style.height = descInput.scrollHeight + 'px'; };
            descInput.removeEventListener('input', descInput._bbAutoResize);
            descInput._bbAutoResize = resize;
            descInput.addEventListener('input', resize);
            setTimeout(resize, 0);
        }
        // Smooth scroll to the inline editor after it opens
        scrollAfterTransition(card.find('.bb-char-editor-inline')[0] || card.find('.bb-char-editor')[0], '.bb-char-editor-inline', card);
        return;
    }

    card.removeClass('editor-open');
}

